package day03_Escape_Sequences;

public class Triangle {

    public static void main(String[] args) {

        System.out.println("\t\t\t    ^");
        System.out.println("\t\t\t   / \\");
        System.out.println("\t\t\t  /   \\");
        System.out.println("\t\t\t /     \\");
        System.out.println("\t\t\t/       \\");
        System.out.println("\t\t\t---------");

    }

}

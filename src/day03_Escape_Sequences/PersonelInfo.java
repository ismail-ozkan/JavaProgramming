package day03_Escape_Sequences;

public class PersonelInfo {

    public static void main(String[] args) {

        System.out.println("Name\t: Ismail");
        System.out.println("Gender\t: Male");
        System.out.println("Date of birth\t: 20/08/1991");
        System.out.println("Address\t: Kocaeli, Turkey");
        System.out.println("Phone number\t: (+90) 555 555 55 55");
        System.out.println("SSN\t: 11111111111");

    }
}

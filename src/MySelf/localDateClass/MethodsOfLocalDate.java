package MySelf.localDateClass;

import java.time.LocalDate;

public class MethodsOfLocalDate {

    public static void main(String[] args) {

        LocalDate date = LocalDate.now();
        System.out.println(LocalDate.now().getYear());//2022-12-05
        System.out.println(date);//2022-12-05
        System.out.println(date.getYear());//2022




    }

}
